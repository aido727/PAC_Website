<span class="page-title">ABOUT PAC</span>
<p>Perth's Allied Costumers (PAC) is a collective of like minded individuals who are interested in costuming and character portrayal with a focus on attending public events including charity and promotional focused events as well as pop culture themed conventions.</p>
